package com.inpt.jibmaak.model;

public class Pagination {
    protected int page;
    protected int limit;

    public Pagination(Integer page, Integer limit) {
        this.page = page;
        this.limit = limit;
    }

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public int getLimit() {
        return limit;
    }

    public void setLimit(int limit) {
        this.limit = limit;
    }
}
